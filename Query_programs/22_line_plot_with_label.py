import matplotlib.pyplot as plt

# Data for the line plot
x = [1, 2, 3, 4, 5]
y = [2, 4, 6, 8, 10]

# Plotting the line
plt.plot(x, y)

# Adding labels to the x-axis and y-axis, and a title
plt.xlabel('X-axis label')
plt.ylabel('Y-axis label')
plt.title('Line Plot with Axis Labels and Title')

# Displaying the plot
plt.show()
